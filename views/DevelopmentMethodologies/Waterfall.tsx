import { Paragraph } from '@components';

const Waterfall = (
  <>
    <Paragraph>
      The Waterfall Model is a linear and sequential software development methodology, in which the
      development process is divided into distinct phases, and each phase must be completed before
      moving onto the next one.
    </Paragraph>

    <Paragraph>The phases in the Waterfall Model typically include:</Paragraph>

    <Paragraph>
      Requirements gathering and analysis: In this phase, the software requirements are identified,
      analyzed and documented.
    </Paragraph>
    <Paragraph>
      Design: In this phase, the software architecture is designed based on the requirements
      gathered in the previous phase.
    </Paragraph>

    <Paragraph>
      Implementation: In this phase, the software is developed according to the design
      specifications.
    </Paragraph>
    <Paragraph>
      Testing: In this phase, the software is tested to ensure that it meets the requirements and
      works as intended.
    </Paragraph>

    <Paragraph>
      Deployment: In this phase, the software is deployed to the production environment.
    </Paragraph>
    <Paragraph>
      Maintenance: In this phase, any issues or bugs discovered during the deployment or usage of
      the software are fixed and the software is updated as needed.
    </Paragraph>
    <Paragraph>
      Each phase in the Waterfall Model is usually completed before moving on to the next phase, and
      changes to previous phases are generally not permitted. This model is often used in projects
      where the requirements are well understood and do not change significantly over the course of
      development.
    </Paragraph>
  </>
);

export default Waterfall;
